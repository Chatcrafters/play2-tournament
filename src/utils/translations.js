// Helper function for interpolation
export const interpolate = (str, params) => {
  if (!str || !params) return str || ''
  
  let result = str
  Object.keys(params).forEach(key => {
    result = result.replace(new RegExp(`{{${key}}}`, 'g'), params[key])
  })
  return result
}

      